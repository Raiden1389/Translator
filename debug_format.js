function applyFormatting(text) {
    let processed = text;

    // Layer 1: Clean & Normalize
    processed = processed.replace(/\r\n/g, '\n');
    processed = processed.replace(/[ \t]+/g, ' ');
    processed = processed.replace(/^\s+|\s+$/gm, '');

    // Layer 2: Paragraph Breathing
    processed = processed.replace(/\n{2,}/g, '\n\n');

    // Layer 3: Punctuation Beauty
    processed = processed.replace(/"([^"]*)"/g, '“$1”'); // Smart quotes
    processed = processed.replace(/\.{3,}/g, '...'); // Ellipsis
    // Fix: Only add space if NOT followed by another dot or digit
    processed = processed.replace(/([.!?,])(?=[^ \d.])/g, '$1 ');

    // Layer 4: Structure Repair (Chinese Comma Style -> Vietnamese Period)
    // Logic: If Comma + Space + Uppercase, change Comma to Period.
    // Heuristic: Most normal sentences don't have Proper Nouns right after a comma unless it's a list. 
    // Risk: "Hello, Lan" -> "Hello. Lan". But acceptable for this genre.
    processed = processed.replace(/, ([A-ZĂÂÁÀẢÃẠẮẰẲẴẶẤẦẨẪẬÉÈẺẼẸẾỀỂỄỆÍÌỈĨỊÓÒỎÕỌỐỒỔỖỘỚỜỞỠỢÚÙỦŨỤỨỪỬỮỰÝỲỶỸỴĐ])/g, '. $1');

    return processed;
}

